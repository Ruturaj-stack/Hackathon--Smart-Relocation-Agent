/** 
 * Canonical column name mapping for auto-detection from Excel files.
 * Keys = canonical field names; values = possible Excel column header variations.
 */
export const COLUMN_MAP: Record<string, string[]> = {
  name: ['neighborhood', 'name', 'community', 'area', 'location', 'district'],
  avgRent: ['avg_rent', 'average_rent', 'rent', 'avg rent', 'median rent', 'monthly rent'],
  minRent: ['min_rent', 'minimum_rent', 'min rent', 'low rent'],
  maxRent: ['max_rent', 'maximum_rent', 'max rent', 'high rent'],
  crimeRate: ['crime_rate', 'crime rate', 'safety', 'crime level', 'crime category'],
  crimeIndex: ['crime_index', 'crime index', 'crime score', 'crime value', 'safety index'],
  transitScore: ['transit_score', 'transit score', 'transit', 'public transit', 'transit rating'],
  livabilityScore: ['livability_score', 'livability', 'livability score', 'quality of life'],
  walkScore: ['walk_score', 'walk score', 'walkability', 'walking score'],
  bikeScore: ['bike_score', 'bike score', 'bikeability', 'cycling score'],
  lat: ['lat', 'latitude', 'y'],
  lng: ['lng', 'lon', 'longitude', 'x'],
  description: ['description', 'summary', 'notes', 'overview'],
  population: ['population', 'pop', 'residents', 'inhabitants'],
  communityArea: ['community_area', 'community area', 'area code', 'ward'],
  medianIncome: ['median_income', 'median income', 'income', 'household income'],
};

/**
 * Detects and normalizes a row's field name from arbitrary column headers.
 * Used when parsing an uploaded Excel file.
 */
export function detectColumnName(header: string): string | null {
  const normalized = header.trim().toLowerCase().replace(/[^a-z0-9 _]/g, '');
  for (const [field, variants] of Object.entries(COLUMN_MAP)) {
    if (variants.some(v => normalized === v || normalized.includes(v))) {
      return field;
    }
  }
  return null;
}

/**
 * Validates a neighborhood record — checks for required fields and cleans missing values.
 */
export function validateNeighborhood(raw: any): Neighborhood | null {
  if (!raw.name || !raw.lat || !raw.lng) return null;
  
  const crimeRateMap: Record<number, 'Low' | 'Medium' | 'High'> = {};
  const resolveCrimeRate = (rate: any, index: any): 'Low' | 'Medium' | 'High' => {
    if (typeof rate === 'string') {
      const r = rate.toLowerCase();
      if (r.includes('low')) return 'Low';
      if (r.includes('high')) return 'High';
      return 'Medium';
    }
    const idx = Number(index) || 50;
    if (idx < 33) return 'Low';
    if (idx > 66) return 'High';
    return 'Medium';
  };

  return {
    id: Number(raw.id) || 0,
    name: String(raw.name).trim(),
    avgRent: Number(raw.avgRent) || 1800,
    minRent: Number(raw.minRent) || Math.round((Number(raw.avgRent) || 1800) * 0.8),
    maxRent: Number(raw.maxRent) || Math.round((Number(raw.avgRent) || 1800) * 1.3),
    crimeRate: resolveCrimeRate(raw.crimeRate, raw.crimeIndex),
    crimeIndex: Number(raw.crimeIndex) || 50,
    transitScore: Number(raw.transitScore) || 70,
    livabilityScore: Number(raw.livabilityScore) || 75,
    walkScore: Number(raw.walkScore) || 70,
    bikeScore: Number(raw.bikeScore) || 60,
    lat: Number(raw.lat),
    lng: Number(raw.lng),
    description: String(raw.description || '').trim() || 'A vibrant Chicago neighborhood.',
    population: Number(raw.population) || 30000,
    communityArea: String(raw.communityArea || '').trim() || 'Chicago',
    medianIncome: Number(raw.medianIncome) || 55000,
  };
}

